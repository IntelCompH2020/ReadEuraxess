	description TEXT,
	job_title TEXT,
	job_summary TEXT,
	job_description TEXT,
	research_field TEXT,
	main_research_field TEXT,
	sub_research_field TEXT,
	researcher_profile TEXT,
	type_of_contract CHAR(255),
	job_status CHAR(255),
	hours_per_week TEXT,
	job_start_date timestamp,
	application_deadline timestamp,
	science4refugees boolean,
	additional_information TEXT,
	comment TEXT,
	info_website CHAR(455),
	eu_funding CHAR(255),
	framework_programme CHAR(255),
	job_reference_number CHAR(255),
	work_location CHAR(255),
	nr_job_positions INT,
