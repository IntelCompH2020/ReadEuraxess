	how_to_apply CHAR(255),
	application_email CHAR(255),
	benefits TEXT,
	required_research_experiences TEXT,
	research_sub_field TEXT,
	years_of_research_experience TEXT,
	additional_requirements TEXT,
	required_education_level TEXT,
	degree TEXT,
	discipline TEXT,
	required_languages TEXT,
	language VARCHAR(400),
	language_level VARCHAR (400),
	job_postal_code VARCHAR (255),
	job_street VARCHAR (400),
	division_faculty VARCHAR (400),
	city VARCHAR (400),
	postal_code VARCHAR (255),
	street VARCHAR (255),
